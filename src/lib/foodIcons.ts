export function getFoodIconUrl(name: string): string | null {
  if (!name) return null;
  const exact = FOOD_ICON_MAP[name];
  if (exact) return `/food-icons/${exact}.png`;

  // partial: check if name contains a mapped key
  for (const [key, icon] of Object.entries(FOOD_ICON_MAP)) {
    if (key.length >= 2 && name.includes(key)) return `/food-icons/${icon}.png`;
  }

  return null;
}
