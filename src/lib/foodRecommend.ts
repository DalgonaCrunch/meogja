export function getAgeGroupFoods(ageField: string | undefined): { label: string; foods: string[] } | null {
  if (!ageField) return null;
  const raw = ageField.trim();

  // "60대 이상" 직접 처리
  if (raw === "60대 이상") {
    return { label: "60대 이상", foods: ["한식","국밥","갈비탕","삼계탕","보쌈","묵은지찜","설렁탕","전복","간장게장","해장국","나물","청국장"] };
  }

  let decade = 0;
  if (raw.endsWith("대")) {
    decade = parseInt(raw);
  } else if (/^\d{4}$/.test(raw)) {
    const age = new Date().getFullYear() - parseInt(raw);
    decade = Math.floor(age / 10) * 10;
  } else if (/^\d{1,2}$/.test(raw)) {
    decade = Math.floor(parseInt(raw) / 10) * 10;
  }
  if (!decade || isNaN(decade)) return null;

  // 2025-26 트렌드 기준으로 업데이트
  if (decade <= 10) return { label: "🎮 젠지", foods: ["마라탕","닭갈비","치킨","불닭","라멘","마라샹궈","핫도그","소금빵","버터떡","떡볶이","흑당버블티","양꼬치"] };
  if (decade === 20) return { label: "✨ MZ", foods: ["마라탕","훠궈","이자카야","포케","스시","파스타","솥밥","타이음식","닭갈비","브런치","소금빵","스테이크","나폴리탄피자","흑임자라떼","쑥라떼"] };
  if (decade === 30) return { label: "🔥 MZ·AZ", foods: ["삼겹살","스시","파스타","이자카야","갈비","스테이크","찜닭","쌀국수","냉면","훠궈","이탈리안","버거","소금빵","한우"] };
  if (decade === 40) return { label: "😎 AZ", foods: ["갈비탕","해산물","삼겹살","삼계탕","냉면","보쌈","낙지볶음","쌈밥","갈비찜","꽃게찜","한우","돼지국밥"] };
  if (decade === 50) return { label: "50대", foods: ["한식","국밥","설렁탕","해산물","간장게장","보쌈","묵은지찜","갈비탕","청국장","해장국","삼계탕","갈치구이"] };
  return { label: "60대 이상", foods: ["한식","국밥","갈비탕","삼계탕","보쌈","묵은지찜","설렁탕","전복","간장게장","해장국","나물","청국장"] };
}
